        <p class="sidePageP" ><strong>YÖS (Yabancı Öğrenci Sınavı) Nedir?</strong>

          YÖS Türkiye’de herhangi bir üniversitede lisans veya ön lisans eğitimi almak isteyen yabancı uyruklu öğrencilerin girmesinin zorunlu olduğu Yabancı Uyruklu Öğrenci Sınavıdır. Kısaltılmış ismine YÖS denilmektedir.

