          <strong>Yabancı Öğrenci Sınavına kimler başvurabilir?</strong>

          YÖS Adaylarının lise son sınıfta olmaları ve ya mezun durumunda bulunmaları şartı ile birlikte;

          - Yabancı Uyruklu Olmaları
          - Doğumla Türk Vatandaşı olup daha sonra Türk vatandaşlığından çıkma izni olanlar (Mavi Kart -5901 sayılı yasa)
          - Yabancı Uyruklu iken Türkiye Cumhuriyeti (T.C.) Vatandaşlığına geçenler Çifte Uyruklular
          - Türkiye Cumhuriyeti vatandaşı olup, öğrenimini yabancı bir ülkede tamamlayanlar (Eğitiminin son üç yılını yurtdışında okumuş olması zorunludur!)
          - Yabancı bir ülkedeki Türk okullarında eğitim ve öğrenimlerini tamamlayan öğrenciler (Örnek: Batı Avrupa Açık Lise Programı gibi)
          - GCE AL sınavına sahip olanlar
          - Türkiye’de lise eğitimi alan tamamen yabancı uyruklu öğrenciler

          YÖS Şartlarını yerine getirmiş sayılmaktadırlar ve YÖS Sınavı’na katılabilirler.

