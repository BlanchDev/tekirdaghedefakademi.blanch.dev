          <p class="sidePageP">
            <strong>YÖS’e herkes başvuru yapabilir mi?</strong>

            YÖS Sınavına yabancı uyruklu öğrenci şartlarına uygun olan tüm öğrenciler başvurabilir. Yabancı Uyruklu öğrenciler, Mavi kartlı öğrenciler ve yurtdışında lisenin tümünü okumuş olan öğrenciler YÖS sınavına başvuru yapabilirler ve yabancı uyruklu öğrenci kontenjanlarından faydalanabilirler.

            <strong>Her yıl YÖS sınavına kaç kişi katılır?</strong>

            Her yıl YÖS sınavına giren öğrenci sayısı ve yabancı uyruklu öğrenci sayısı artmaktadır. Yıllık olarak ortalama 180.000 Yabancı Uyruklu öğrenci T.C.’de ki üniversiteleri tercih etmektedirler. Bu öğrencilerden ortalama +50.000 öğrenci YÖS sınavlarına katılmaktadırlar.

            <strong> ULUYÖS, DEYÖS, İÜYÖS, MUYÖS, EGEYÖS, OMU YÖS nedir?</strong>

            YÖS; merkezde ÖSYM tarafından yayınlanan bir sınav olmadığı için 2011 yılından bu yana YÖS sınavı ilgili üniversiteler tarafından kendi adları ile yapılmaktadır.

            Örneğin: ULUYÖS, DEYÖS, IÜYÖS, MUYÖS, EGEYÖS, AÜYÖS, AYÖS, OMÜYÖS.

            Bazı üniversiteler; özellikle büyük Devlet Üniversitelerinin yapmış oldukları YÖS sınav sonucunu kabul ederek, başvuru kabul etmektedirler. Fakat 2021 tarihi itibariyle artık neredeyse üniversitelerin çoğu kendi sınavını yapmaktadır.

            11,04,2012 tarih ve 28261 sayılı Resmi Gazetede yayımlanan 6287 sayılı kanunun 14/f maddesince Devlet Üniversitelerinde %35, Vakıf Üniversitelerinde % 50’yi geçmeyecek şekilde Uluslararası öğrencilere kontenjan ayrılmıştır.

            İlk YÖS Sınavı 1981 yılında yapılmıştır. Yılda bu sınava ortalama 49.000 öğrenci girmektedir. YÖS Sınavı, verilere dayanarak, ÖSYM’nin açıkladığı verilere dayanarak: o günden bu yana ortalama 135.000 öğrencinin Türkiye’de eğitim almasını sağlamıştır.

            <strong>YÖS Sınav Başvuruları Nasıl Yapılır?</strong>

            YÖS Sınav Başvuru koşulları, tarihleri ve süreçleri üniversitelerin yönetimi tarafından belirlenen şartlar kendi sitelerinden yayınlanmaktadır. Sınav başvurularında her hangi bir sınırlama yoktur. Üniversiteler kendi YÖS Sınavlarını yaptıkları için adaylar tüm üniversitelerin sınavlarına başvurabilirler ve bu sınavlara katılabilirler.

            <strong>YÖS Başvuruları hangi tarihlerde olur?</strong>

            YÖS Sınav başvuruları genellikle Mart – Haziran aylarında yapılmaktadır fakat pandemi surecinde değişkenlik gösteren üniversiteler olmuştur.. Bu aylar arasında üniversiteler başvuru tarihleri – sınav tarihleri ve kontenjanlarını açıklamaktadırlar.

            <strong>YÖS Sınav Tarihleri?</strong>

            YÖS sınav tarihleri yer yıl üniversiteler tarafından belirlenmektedir. YÖS Sınav tarihlerini sitemiz üzerinden duyurular kısmından da görebilirsiniz

            <strong>YÖS ne zaman açıklanır?</strong>

            YÖS Sınav sonuçları – Üniversitelerin Kesin Kabul/Kayıt sonuçları YÖS Sınavları yapıldıktan ortalama 3 hafta ile 6 hafta arasında sonuçlanmaktadır. Genellikle online sistem üzerinden sonuçlar belli olmaktadır.

            <strong>YÖS Sınavı Nerede yapılır Online sınav var mı?</strong>

            YÖS Sınavları öncelikle üniversitelerin kendi kampüsü içerisinde, yurt içi ve yurtdışında belirlenmiş olan merkezlerde yapılmaktadır. Türkiye içerisinde en büyük sınav merkezi İstanbul dur. Neredeyse bütün üniversiteler sınavlarını İstanbul da gerçekleştirir. Pandemi ile birlikte bir çok okul online olarak 2021 yılında sınav yapmıştır.

            <strong>Hangi Üniversiteler YÖS Yapar?</strong>

            Türkiye’de ki birçok üniversite YÖS Sınavı ile yabancı uyruklu öğrenci alımı yapmaktadır. Bazı üniversitelerin YÖS Sınavları diğer üniversitelerde geçerlidir.

            İstanbul Üniversitesi, Cerrahpaşa Üniversitesi / Ankara Üniversitesi / Marmara Üniversitesi, Ege Üniversitesi YÖS Sınav sonuçları ile Türkiye’de ki bir çok üniversiteye başvuru yapılabilmektedir.

            Üniversiteler tabi ikide kendi YÖS sınav sonuçlarını daha referans almaktadırlar.

            <strong>YÖS Başvuruları Ücretli mi?</strong>

            YÖS sınav başvuru ücretleri her yıl üniversiteler tarafından belirlenmektedir. Başvuru ücretleri 2021 yılı itibariyle 150TL – 500 TL arasında değişkenlik göstermektedir.

            <strong>YÖS Sınav içeriğinde neler var?</strong>

            YÖS Sınavı genellikle 80 sorudan oluşmaktadır. Bu sorularda ise ortalama 35 Matematik Sorusu – 5 Geometri – 40 IQ sorusu sorulmaktadır.

            Üniversitelerde bu rakamlar değişebilmektedir. Genellikle YÖS sınavlarında %50 olarak IQ – Genel Yetenek Soruları sorulmaktadır.

            <strong>YÖS Sınav Süresi</strong>

            YÖS sınavlarında verilen ortalama süre 90 ile 120 dakika arasında değişmektedir. Bir çok üniversitede 90 dk olarak yapılmaktadır. Öğrencilerin ortalama bir YÖS Sınav sorusu için 1,20 dakika süresi vardır.

            <strong>YÖS Sınavı Taban Puanları</strong>

            Başvurularda esas alınan taban puan 50dir.
